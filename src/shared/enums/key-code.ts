/**
 * Enumeration of keyboard key codes used by the extension.
 * Maps to the standard KeyboardEvent.code values.
 */
export enum KeyCode {
	/** Down arrow key */
	ArrowDown = 'ArrowDown',
	/** Left arrow key */
	ArrowLeft = 'ArrowLeft',
	/** Right arrow key */
	ArrowRight = 'ArrowRight',
	/** Up arrow key */
	ArrowUp = 'ArrowUp',
	/** Digit keys (0-9) - represented by '1' as a placeholder */
	Digit = '1',
	/** B key */
	KeyB = 'KeyB',
	/** L key */
	KeyL = 'KeyL',
	/** N key */
	KeyN = 'KeyN',
	/** O key */
	KeyO = 'KeyO',
	/** P key */
	KeyP = 'KeyP',
	/** V key */
	KeyV = 'KeyV',
	/** Space bar */
	Space = 'Space',
	/** Shift key */
	Shift = 'Shift',
}
